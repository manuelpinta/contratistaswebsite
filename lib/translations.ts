export function getLanguageByCountry(countryCode: CountryCode | null): Language {
  if (!countryCode || !(countryCode in COUNTRY_LANGUAGES)) {
    return 'es' // Default
  }
  return COUNTRY_LANGUAGES[countryCode]
}

